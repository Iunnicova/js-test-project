// üîπ –û–±—ä—è–≤–ª—è–µ—Ç—Å—è –∫–æ–Ω—Å—Ç–∞–Ω—Ç–∞ rootSelector, —Å–æ–¥–µ—Ä–∂–∞—â–∞—è CSS-—Å–µ–ª–µ–∫—Ç–æ—Ä –¥–ª—è –ø–æ–∏—Å–∫–∞ –∫–æ—Ä–Ω–µ–≤–æ–≥–æ DOM-—ç–ª–µ–º–µ–Ω—Ç–∞ –≤–∏–¥–µ–æ–ø–ª–µ–µ—Ä–∞.
// ‚û°Ô∏è –ê—Ç—Ä–∏–±—É—Ç data-js-video-player –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –∫–∞–∫ "—è–∫–æ—Ä—å" –¥–ª—è –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏–∏ –≤—Å–µ—Ö –≤–∏–¥–µ–æ–ø–ª–µ–µ—Ä–æ–≤ –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü–µ.
const rootSelector = '[data-js-video-player]';

class VideoPlayer {
  // üîπ –°–æ–∑–¥–∞—ë—Ç—Å—è –ø–æ–ª–µ selectors ‚Äî –æ–±—ä–µ–∫—Ç —Å CSS-—Å–µ–ª–µ–∫—Ç–æ—Ä–∞–º–∏ –¥–ª—è –ø–æ–∏—Å–∫–∞ –≤–ª–æ–∂–µ–Ω–Ω—ã—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –≤–Ω—É—Ç—Ä–∏ –æ–¥–Ω–æ–≥–æ –ø–ª–µ–µ—Ä–∞.
  selectors = {
    root: rootSelector, // –°–µ–ª–µ–∫—Ç–æ—Ä –∫–æ—Ä–Ω–µ–≤–æ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞ –ø–ª–µ–µ—Ä–∞
    video: '[data-js-video-player-video]', // –°–µ–ª–µ–∫—Ç–æ—Ä –¥–ª—è –≤–∏–¥–µ–æ—ç–ª–µ–º–µ–Ω—Ç–∞ <video>
    panel: '[data-js-video-player-panel]', // –°–µ–ª–µ–∫—Ç–æ—Ä –ø–∞–Ω–µ–ª–∏ —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è (UI)
    playButton: '[data-js-video-player-play-button]' // –°–µ–ª–µ–∫—Ç–æ—Ä –∫–Ω–æ–ø–∫–∏ –≤–æ—Å–ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏—è
  };

  // üîπ –°–æ–∑–¥–∞—ë—Ç—Å—è –ø–æ–ª–µ stateClasses, —Å–æ–¥–µ—Ä–∂–∞—â–µ–µ –∫–ª–∞—Å—Å—ã —Å–æ—Å—Ç–æ—è–Ω–∏–π –ø–ª–µ–µ—Ä–∞.
  stateClasses = {
    isActive: 'is-active' // –ö–ª–∞—Å—Å, —É–∫–∞–∑—ã–≤–∞—é—â–∏–π –Ω–∞ –∞–∫—Ç–∏–≤–Ω–æ–µ —Å–æ—Å—Ç–æ—è–Ω–∏–µ —ç–ª–µ–º–µ–Ω—Ç–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä, –ø–∞–Ω–µ–ª—å —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è)
  };

  // üîπ –ö–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä –∫–ª–∞—Å—Å–∞ VideoPlayer. –ü—Ä–∏–Ω–∏–º–∞–µ—Ç rootElement ‚Äî –∫–æ—Ä–Ω–µ–≤–æ–π DOM-—ç–ª–µ–º–µ–Ω—Ç –ø–ª–µ–µ—Ä–∞.
  constructor(rootElement) {
    this.rootElement = rootElement; // –°–æ—Ö—Ä–∞–Ω—è–µ–º –∫–æ—Ä–Ω–µ–≤–æ–π —ç–ª–µ–º–µ–Ω—Ç

    // üîπ –ù–∞—Ö–æ–¥–∏–º –≤–ª–æ–∂–µ–Ω–Ω—ã–µ —ç–ª–µ–º–µ–Ω—Ç—ã –≤–Ω—É—Ç—Ä–∏ –ø–ª–µ–µ—Ä–∞:
    this.videoElement = this.rootElement.querySelector(this.selectors.video); // –í–∏–¥–µ–æ—ç–ª–µ–º–µ–Ω—Ç
    this.panelElement = this.rootElement.querySelector(this.selectors.panel); // –ü–∞–Ω–µ–ª—å —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è
    this.playButtonElement = this.rootElement.querySelector(
      this.selectors.playButton
    ); // –ö–Ω–æ–ø–∫–∞ –≤–æ—Å–ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏—è
    this.bindEvents(); //–ø—Ä–∏–≤—è–∑—ã–≤–∞–µ–º –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∏ —Å–æ–±—ã—Ç–∏–π –∫ —ç–ª–µ–º–µ–Ω—Ç–∞–º
  }

  //–≠—Ç–æ—Ç –º–µ—Ç–æ–¥ –≤—ã–∑—ã–≤–∞–µ—Ç—Å—è –ø—Ä–∏ –Ω–∞–∂–∞—Ç–∏–∏ –Ω–∞ –∫–Ω–æ–ø–∫—É "Play"
  onPlayButtonClick = () => {
    this.videoElement.play(); //–ó–∞–ø—É—Å–∫–∞–µ–º –≤–æ—Å–ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–µ –≤–∏–¥–µ–æ
    this.videoElement.controls = true; //–ü–æ–∫–∞–∑—ã–≤–∞–µ–º –≤—Å—Ç—Ä–æ–µ–Ω–Ω—ã–µ –±—Ä–∞—É–∑–µ—Ä–Ω—ã–µ —ç–ª–µ–º–µ–Ω—Ç—ã —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è
    this.panelElement.classList.remove(this.stateClasses.isActive); //‚ùå –°–∫—Ä—ã–≤–∞–µ–º –∫–∞—Å—Ç–æ–º–Ω—É—é –ø–∞–Ω–µ–ª—å —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è
  };

  //–≠—Ç–æ—Ç –º–µ—Ç–æ–¥ –≤—ã–∑—ã–≤–∞–µ—Ç—Å—è, –∫–æ–≥–¥–∞ –≤–∏–¥–µ–æ –æ—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç—Å—è (–Ω–∞–ø—Ä–∏–º–µ—Ä, –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–∞–∂–∞–ª "–ü–∞—É–∑–∞")
  onVideoPause = () => {
    this.videoElement.controls = false; //‚ùå –°–∫—Ä—ã–≤–∞–µ–º –≤—Å—Ç—Ä–æ–µ–Ω–Ω—ã–µ –±—Ä–∞—É–∑–µ—Ä–Ω—ã–µ —ç–ª–µ–º–µ–Ω—Ç—ã —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è
    this.panelElement.classList.add(this.stateClasses.isActive); // ‚úÖ –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –ø–∞–Ω–µ–ª—å —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è
  };

  //–ø—Ä–∏–≤—è–∑—ã–≤–∞–µ–º –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∏ —Å–æ–±—ã—Ç–∏–π –∫ —ç–ª–µ–º–µ–Ω—Ç–∞–º
  bindEvents() {
    this.playButtonElement.addEventListener('click', this.onPlayButtonClick); // ‚ñ∂Ô∏è –û–±—Ä–∞–±–æ—Ç–∫–∞ –∫–ª–∏–∫–∞ –ø–æ –∫–Ω–æ–ø–∫–µ "Play"
    this.videoElement.addEventListener('pause', this.onVideoPause); // ‚è∏Ô∏è –û–±—Ä–∞–±–æ—Ç–∫–∞ —Å–æ–±—ã—Ç–∏—è –ø–∞—É–∑—ã –≤–∏–¥–µ–æ
  }
}

// üîπ –ö–ª–∞—Å—Å VideoPlayerCollection —É–ø—Ä–∞–≤–ª—è–µ—Ç –∫–æ–ª–ª–µ–∫—Ü–∏–µ–π –≤—Å–µ—Ö –≤–∏–¥–µ–æ–ø–ª–µ–µ—Ä–æ–≤ –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü–µ.
class VideoPlayerCollection {
  constructor() {
    this.init(); //  –ü—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ —ç–∫–∑–µ–º–ø–ª—è—Ä–∞ –≤—ã–∑—ã–≤–∞–µ–º –º–µ—Ç–æ–¥ init()
  }

  // üîπ –ú–µ—Ç–æ–¥ init –Ω–∞—Ö–æ–¥–∏—Ç –≤—Å–µ —ç–ª–µ–º–µ–Ω—Ç—ã, –ø–æ–¥—Ö–æ–¥—è—â–∏–µ –ø–æ–¥ rootSelector.
  // –î–ª—è –∫–∞–∂–¥–æ–≥–æ –Ω–∞–π–¥–µ–Ω–Ω–æ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞ —Å–æ–∑–¥–∞—ë—Ç—Å—è —ç–∫–∑–µ–º–ø–ª—è—Ä VideoPlayer.
  init() {
    document.querySelectorAll(rootSelector).forEach((element) => {
      new VideoPlayer(element); //  –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º –∫–∞–∂–¥—ã–π –≤–∏–¥–µ–æ–ø–ª–µ–µ—Ä –ø–æ –æ—Ç–¥–µ–ª—å–Ω–æ—Å—Ç–∏
    });
  }
}

export default VideoPlayerCollection; //–≠–∫—Å–ø–æ—Ä—Ç–∏—Ä—É–µ—Ç—Å—è –∫–ª–∞—Å—Å VideoPlayerCollection –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é,
